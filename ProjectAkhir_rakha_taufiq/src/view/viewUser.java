    public void tampilkanDataMovie(List<movie> daftarMovie) {
        tm.setRowCount(0); // Hapus data lama
        for (movie m : daftarMovie) {
            Object[] row = {
                m.getid(),
                m.getname(),
                m.getduration(),
                m.getdescription(),
                m.getSchedule()
            };
            tm.addRow(row);
        }
    }

