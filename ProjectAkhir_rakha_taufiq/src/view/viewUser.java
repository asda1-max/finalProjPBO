    private void user_bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_user_bookActionPerformed
        int selectedRow = movieTable.getSelectedRow();
        if (selectedRow != -1) {
            String id = movieTable.getValueAt(selectedRow, 0).toString();
            String title = movieTable.getValueAt(selectedRow, 1).toString();
            String schedule = movieTable.getValueAt(selectedRow, 4).toString();
            int duration = Integer.parseInt(movieTable.getValueAt(selectedRow, 2).toString());
            String description = movieTable.getValueAt(selectedRow, 3).toString();

            System.out.println("Selected Movie Details:");
            System.out.println("ID: " + id);
            System.out.println("Title: " + title);
            System.out.println("Duration: " + duration);
            System.out.println("Description: " + description);
            System.out.println("Schedule: " + schedule);
            // Buat dan tampilkan form bookseat
            controllers.bookViews(title, duration, description, schedule, curuser);
            dispose(); // tutup userview jika perlu
        } else {
            JOptionPane.showMessageDialog(this, "Pilih film terlebih dahulu!");
        }
    }//GEN-LAST:event_user_bookActionPerformed

